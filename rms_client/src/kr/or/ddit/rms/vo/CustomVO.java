package kr.or.ddit.rms.vo;

import java.io.Serializable;

public class CustomVO implements Serializable {

	String custom_id;
	String custom_name;
	String custom_email;
	String custom_addr;
	String custom_birth;
	String custom_tel;
	String custom_point;
	
	public String getCustom_id() {
		return custom_id;
	}
	public void setCustom_id(String custom_id) {
		this.custom_id = custom_id;
	}
	public String getCustom_name() {
		return custom_name;
	}
	public void setCustom_name(String custom_name) {
		this.custom_name = custom_name;
	}
	public String getCustom_email() {
		return custom_email;
	}
	public void setCustom_email(String custom_email) {
		this.custom_email = custom_email;
	}
	public String getCustom_addr() {
		return custom_addr;
	}
	public void setCustom_addr(String custom_addr) {
		this.custom_addr = custom_addr;
	}
	public String getCustom_birth() {
		return custom_birth;
	}
	public void setCustom_birth(String custom_birth) {
		this.custom_birth = custom_birth;
	}
	public String getCustom_tel() {
		return custom_tel;
	}
	public void setCustom_tel(String custom_tel) {
		this.custom_tel = custom_tel;
	}
	public String getCustom_point() {
		return custom_point;
	}
	public void setCustom_point(String custom_point) {
		this.custom_point = custom_point;
	}
	
	

}
