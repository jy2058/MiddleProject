package kr.or.ddit.rms.vo;

import java.io.Serializable;

public class HospitalVO implements Serializable {

	String hos_loc;
	String hos_x;
	String hos_y;
	String admin_id;
	String hos_tel;
	
	public String getHos_loc() {
		return hos_loc;
	}
	public void setHos_loc(String hos_loc) {
		this.hos_loc = hos_loc;
	}
	public String getHos_x() {
		return hos_x;
	}
	public void setHos_x(String hos_x) {
		this.hos_x = hos_x;
	}
	public String getHos_y() {
		return hos_y;
	}
	public void setHos_y(String hos_y) {
		this.hos_y = hos_y;
	}
	public String getAdmin_id() {
		return admin_id;
	}
	public void setAdmin_id(String admin_id) {
		this.admin_id = admin_id;
	}
	public String getHos_tel() {
		return hos_tel;
	}
	public void setHos_tel(String hos_tel) {
		this.hos_tel = hos_tel;
	}
	
	

}
